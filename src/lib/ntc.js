export function getName(hex) {
  hex = hex.replace("#", "").toUpperCase();
  const r = parseInt(hex.slice(0, 2), 16);
  const g = parseInt(hex.slice(2, 4), 16);
  const b = parseInt(hex.slice(4, 6), 16);

  let minDiff = Infinity;
  let closestName = "Unknown";

  for (const [h, name] of NAMES) {
    const hr = parseInt(h.slice(0, 2), 16);
    const hg = parseInt(h.slice(2, 4), 16);
    const hb = parseInt(h.slice(4, 6), 16);

    const diff = Math.pow(r - hr, 2) + Math.pow(g - hg, 2) + Math.pow(b - hb, 2);
    if (diff < minDiff) {
      minDiff = diff;
      closestName = name;
    }
  }

  return closestName;
}
